	PROGRAM LAUE_TIFF
C
	LOGICAL LASK
	COMMON /LASK_COM/ LASK
C
	CHARACTER FILE_NAME*255
C
	CALL GET_COMMAND_ARGUMENT(1,FILE_NAME,LEN,ISTATUS)
C
	LASK=(ISTATUS .NE. 0)
	IF( LASK ) THEN
	  PRINT '(1X,A,$)','File Name: '
	  READ(*,*) FILE_NAME
	  LEN=LEN_TRIM(FILE_NAME)
	ENDIF
