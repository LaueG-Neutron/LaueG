	END


	SUBROUTINE UNPACK_WAV_PARS(IPAR, PARS,ESDS,LPRINT)
C
C Unpack WAV_PAR() from PARS() and optionally output the refined results
C
	LOGICAL LPRINT
	REAL PARS(*),ESDS(*)
C
	COMMON /WAV_CORR_COM/ IWAV_OPT,NWAV,WAV_GAMMA,WAV_MIN,WAV_STEP,WAV_PAR(20)
C
	CHARACTER STRING*80
	REAL TEMP(200)
C
C Unpack PARS into WAV_PAR and put ESDS into TEMP
C NB: The middle parameter is not refined
C
	DO I=1,NWAV
	  IF(I .NE. NWAV/2) THEN
	    IPAR=IPAR+1
	    WAV_PAR(I)=PARS(IPAR)
	    TEMP(I)=ESDS(IPAR)
	  ENDIF
	ENDDO
C
C If no print-out is required, simply return
C
	IF( .NOT.LPRINT ) RETURN
C
C Print out refined values + esds
C
	WRITE(10,'(/,A,/)') 'Wavelength spline parameters:'
	DO I=1,NWAV,10
	  I2=MIN(I+9,NWAV)
C Output lines for wavelength and spline values
	  WRITE(10,'(A,10F7.3)') 'Wav     ',(WAV_MIN+WAV_STEP*(K-1),K=I,I2)
	  WRITE(10,'(A,10F7.3)') 'Spline  ',(WAV_PAR(K),K=I,I2)
C "Write" esd values to STRING()
	  WRITE(STRING,'(10F7.3)') (TEMP(K),K=I,I2)
C Overwrite the middle parameter esd in STRING() with "fixed"
	  IF(NWAV/2.GE.I .AND. NWAV/2.LE.I2)
	1					STRING(7*(NWAV/2-I)+1:7*(NWAV/2-I)+7)='  fixed'
C Output STRING()
	  WRITE(10,'(2A)') 'esd     ',STRING(1:7*(I2-I+1))
	ENDDO
C
	RETURN
	END


