	REAL PARS(*)
C
	COMMON /WAV_CORR_COM/ IWAV_OPT,NWAV,WAV_GAMMA,WAV_MIN,WAV_STEP,WAV_PAR(20)
C
C Quad spline (the middle parameter is not refined!)
C
	DO I=1,NWAV
	  IF(I .NE. NWAV/2) THEN
	    IPAR=IPAR+1
	    PARS(IPAR)=WAV_PAR(I)
	  ENDIF
	ENDDO
