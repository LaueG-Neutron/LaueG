	SUBROUTINE PACK_ABS_PARS(PARS,IPAR)
C
C Add ABS to PARS()
C
	REAL PARS(*)
C
	COMMON /ABS_CORR_COM/ IABS_OPT,UR0,UR_LIN
C
C Always refine the wavelength dependent part
C
	IPAR=IPAR+1
	PARS(IPAR)=UR_LIN
C
C Only refine the constant part for the general case
C
	IF(IABS_OPT .EQ. 3) THEN
	  IPAR=IPAR+1
	  PARS(IPAR)=UR0
	ENDIF
C
	RETURN
	END


	SUBROUTINE UNPACK_ABS_PARS(IPAR, PARS,ESDS,LPRINT)
C
C Unpack EXTI from PARS() and optionally output the refined result
C
	LOGICAL LPRINT
	REAL PARS(*),ESDS(*)
C
	COMMON /ABS_CORR_COM/ IABS_OPT,UR0,UR_LIN
C
C Always refine the wavelength dependent part
C
	IPAR=IPAR+1
	UR_LIN=PARS(IPAR)
C
C Set UR0 depending on the model used
C
C IABS_OPT=1	normal absorbers
	IF(IABS_OPT .EQ. 1) THEN
	  UR0=0.0
C IABS_OPT=2	hydrogen, Judith Howard's eqn (19.2*WAV+20.6 barns)
	ELSE IF(IABS_OPT .EQ. 2) THEN
	  UR0=UR_LIN*1.07
C IABS_OPT=3	general case, UR0 & UR_LIN are indenpendent
	ELSE IF(IABS_OPT .EQ. 3) THEN
	  IPAR=IPAR+1
	  UR0=PARS(IPAR)
	ENDIF
C
	IF(LPRINT) THEN
	  IF(IABS_OPT .EQ. 1) THEN
	    WRITE(10,'(/,A,2(F6.3,A,F5.3,A))') 'Refined uR =',
	1							UR_LIN,' (',ESDS(IPAR),') * wav'
	    IFRAC_H=0
	  ELSE IF(IABS_OPT .EQ. 2) THEN
	    WRITE(10,'(/,A,2(F6.2,A,F5.2,A))') 'Refined uR =',
	1							UR_LIN,' (',ESDS(IPAR),') * (wav + 1.07)'
	    IFRAC_H=100
	  ELSE IF(IABS_OPT .EQ. 3) THEN
	    WRITE(10,'(/,A,2(F6.2,A,F5.2,A))') 'Refined uR =',
	1		UR0,' (',ESDS(IPAR-1),') +',UR_LIN,' (',ESDS(IPAR),') * wav'
	    IFRAC_H= NINT( 100.0*UR0/1.07/MAX(1E-8,UR_LIN) )
	  ENDIF
	  WRITE(10,'(2X,A,I4,A)') 'Fraction of "hydrogenous absorption" =',
	1								MAX(-999,MIN(+999, IFRAC_H)),'%'
	ENDIF
C
	RETURN
	END


