C--------------- High-level routines to pack/unpack LSQ parameters -------------

	SUBROUTINE PACK_LSQ_PARS(PARS,NPARS)
C
C Pack all refined LSQ parameters into PARS, also return
C the number of refined parameters in NPARS
C
	REAL PARS(*)
C
	LOGICAL LFILE,LWAV,LEFF,LABS,LEXTI,LTWIN_RATIO
	COMMON /LSQ_LPAR_COM/ LFILE,LWAV,LEFF,LABS,LEXTI,LTWIN_RATIO
C
	COMMON /PARS_NUM_COM/ NFILE,NWAV,NEFF,NABS,NEXTI,NTWIN
C
C Load LSQ parameters into PARS
C Also, save the end position of each set for use by GET_PAR_NAME
C
	NPARS=0
	IF( LFILE ) CALL PACK_FILE_PARS(PARS,NPARS)
	NFILE=NPARS
	IF( LWAV  ) CALL PACK_WAV_PARS (PARS,NPARS)
	NWAV=NPARS
	IF( LEFF ) CALL PACK_EFF_PARS(PARS,NPARS)
	NEFF=NPARS
	IF( LABS  ) CALL PACK_ABS_PARS (PARS,NPARS)
	NABS=NPARS
	IF( LEXTI ) CALL PACK_EXTI_PARS(PARS,NPARS)
	NEXTI=NPARS
	IF( LTWIN_RATIO ) CALL PACK_TWINS_PARS(PARS,NPARS)
	NTWIN=NPARS
C
	RETURN
	END


	SUBROUTINE UNPACK_LSQ_PARS(PARS,ESDS,LPRINT)
C
C Unpack all refined LSQ parameters from PARS.
C If LPRINT is true, print out refined parameter values and esds.
C
	LOGICAL LPRINT
	REAL PARS(500),ESDS(500)
C
	LOGICAL LFILE,LWAV,LEFF,LABS,LEXTI,LTWIN_RATIO
	COMMON /LSQ_LPAR_COM/ LFILE,LWAV,LEFF,LABS,LEXTI,LTWIN_RATIO
C
C Unpack the refined parameters and output the refined result and esds
C
	IPAR=0
	IF( LFILE ) CALL UNPACK_FILE_PARS(IPAR, PARS,ESDS,LPRINT)
	IF( LWAV  ) CALL UNPACK_WAV_PARS (IPAR, PARS,ESDS,LPRINT)
	IF( LEFF  ) CALL UNPACK_EFF_PARS (IPAR, PARS,ESDS,LPRINT)
	IF( LABS  ) CALL UNPACK_ABS_PARS (IPAR, PARS,ESDS,LPRINT)
	IF( LEXTI ) CALL UNPACK_EXTI_PARS(IPAR, PARS,ESDS,LPRINT)
	IF( LTWIN_RATIO ) CALL UNPACK_TWINS_PARS(IPAR, PARS,ESDS,LPRINT)
C
	RETURN
	END


	SUBROUTINE GET_PAR_NAME(NAME, IPAR)
C
	CHARACTER NAME*(*)
C
	COMMON /PARS_NUM_COM/ NFILE,NWAV,NEFF,NABS,NEXTI,NTWIN
C
	NAME=REPEAT(' ',LEN(NAME))
	IF(LEN(NAME) .LT. 5) STOP 'BUG(get_par_name): LEN(NAME) < 5'
C
	IF(IPAR .LE. NFILE) THEN
	  WRITE(NAME,'(A,I3)') 'File',IPAR
	ELSE IF(IPAR .LE. NWAV) THEN
	  WRITE(NAME,'(A,I2)') 'Wav',IPAR-NTWIN
	ELSE IF(IPAR .LE. NEFF) THEN
	  WRITE(NAME,'(A,I2)') 'Effic',IPAR-NWAV
	ELSE IF(IPAR .LE. NABS) THEN
	  WRITE(NAME,'(A,I2)') 'Abs',IPAR-NEFF
	ELSE IF(IPAR .LE. NEXTI) THEN
	  NAME='Exti'
	ELSE IF(IPAR .LE. NTWIN) THEN
	  NAME='Twin'
	ELSE
	  STOP 'BUG(get_par_name): Invalid IPAR'
	ENDIF
C
	RETURN
	END


C--------------- Low-level routines to pack/unpack LSQ parameters -------------

	SUBROUTINE PACK_FILE_PARS(PARS,IPAR)
C
C Add FSCALE() to PARS()
C
	REAL PARS(*)
C
	PARAMETER (NFILES_MAX=400)
	COMMON /FILE_CORR_COM/ FSCALE(NFILES_MAX)
C
	COMMON /FILE_NUM_COM/ IFILE_NUM(NFILES_MAX),NFILES
C
C Pack all but last FSCALE() into PARS()
C
	DO I=1,NFILES-1
	  IPAR=IPAR+1
	  PARS(IPAR)=FSCALE(I)
	ENDDO
C
	RETURN
	END


	SUBROUTINE UNPACK_FILE_PARS(IPAR, PARS,ESDS,LPRINT)
C
C Unpack FSCALE() from PARS() and optionally output the refined result
C
	LOGICAL LPRINT
	REAL PARS(*),ESDS(*)
C
	LOGICAL LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
	COMMON /LCORR_COM/ LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
C
	PARAMETER (NFILES_MAX=400)
	COMMON /FILE_CORR_COM/ FSCALE(NFILES_MAX)
	COMMON /FILE_NUM_COM/ IFILE_NUM(NFILES_MAX),NFILES
C
	CHARACTER STRING*80
	REAL TEMP(500)
C
	DO I=1,NFILES-1
	  IPAR=IPAR+1
	  FSCALE(I)=PARS(IPAR)
	  TEMP(I)=ESDS(IPAR)
	ENDDO
C
C If no print-out is required, simply return
C
	IF( .NOT.LPRINT ) RETURN
C
C Print out refined FSCALE values + esds
C
	WRITE(10,'(/,A,/)') 'Scale factors used for input files:'
	DO I=1,NFILES,10
	  I2=MIN(I+9,NFILES)
C Output lines for actual file numbers and scale factor values
	  WRITE(10,'(A,I6,9I7)') 'File #  ',(IFILE_NUM(K),K=I,I2)
	  WRITE(10,'(A,10F7.3)') 'Scale   ',(FSCALE(K),K=I,I2)
C "Write" esd values to STRING()
	  WRITE(STRING,'(10F7.3)') (TEMP(K),K=I,I2)
C Overwrite the last scale factor esd in STRING() with "fixed"
	  IF(I2 .EQ. NFILES) STRING(7*(I2-I)+1:7*(I2-I)+7)='  fixed'
C Output STRING()
	  WRITE(10,'(2A)') 'esd     ',STRING(1:7*(I2-I+1))
	ENDDO
C
	RETURN
	END


