	END



	SUBROUTINE INIT_ABS_PARS
C
C Disable correction (for INITIAL_FILE_SCALES)
C
	LOGICAL LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
	COMMON /LCORR_COM/ LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
C
	COMMON /ABS_CORR_COM/ IABS_OPT,UR0,UR_LIN
C
C If no absorption correction, set uR to zero and return
C
	IF( .NOT.LABS_CORR ) THEN
		UR0=0.0
		UR_LIN=0.0
	  RETURN
	ENDIF
C
C Setup UR0 & UR_LIN as if set by UNPACK_ABS_PARS
C
	UR0=0.1
	UR_LIN=0.1
C IABS_OPT=1	normal absorbers
	IF(IABS_OPT .EQ. 1) UR0=0.0
C IABS_OPT=2	hydrogen, Judith Howard's eqn (19.2*WAV+20.6 barns)
	IF(IABS_OPT .EQ. 2) UR0=UR_LIN*1.07
C
	RETURN
	END



	SUBROUTINE INIT_EXTI_PARS
C
C We must first calculate COUNTS_SEQ (with extinction off) before
C we can estimate the initial value for EXTI
C
	LOGICAL LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
	COMMON /LCORR_COM/ LFILE_CORR,LWAV_CORR,LEFF_CORR,LEXTI_CORR,LHARM_CORR,LABS_CORR
C
	COMMON /EXTI_CORR_COM/ IEXTI_OPT,EXTI
C
	COMMON /MISC_PARS_COM/ NSEQ_MIN,SEQ_FRAC,X_LO,X_HI,Y_LO,Y_HI,WAV_LO,WAV_HI,
	1	CELL_MULT,EFF_WIDTH,EFF_THICK,EXTI_CORR_MAX,NBINS_NPAR,
	2	ALL_REL_ERR,ALL_SIG_MULT,WEAK_SIG_MULT,EXTI_ERR,WAV_ERR1,WAV_ERR2,
	3	HARM_CUTOFF_MULT,IVERBOSE
C
	PARAMETER (NSEQ_MAX=100000)
	COMMON /SEQ_COM/ IFIRST(NSEQ_MAX),ILAST(NSEQ_MAX),COUNTS_SEQ(NSEQ_MAX),NSEQ
C
	PARAMETER (NDATA_MAX=2000000)
	INTEGER HKLMS(4,NDATA_MAX),IFILES(NDATA_MAX)
	REAL WAVS(NDATA_MAX),COUNTS(NDATA_MAX),DCOUNTS(NDATA_MAX)
	REAL TTHS(NDATA_MAX),XY_PIX(2,NDATA_MAX)
	COMMON /DATA_COM/ HKLMS,IFILES,WAVS,COUNTS,DCOUNTS,TTHS,XY_PIX,NDATA
C
C If no extinction correction, set EXTI to zero and return
C
	IF( .NOT.LEXTI_CORR ) THEN
	  EXTI=0.0
	  RETURN
	ENDIF
C
C Get the largest extinction "x" factor for EXTI=1
C
	EXTI=1.0
	X_MAX=0.0
	DO ISEQ=1,NSEQ
	  DO I=IFIRST(ISEQ),ILAST(ISEQ)
	    X=CALC_EXTI_FACTOR(COUNTS_SEQ(ISEQ),WAVS(I),TTHS(I))
	    X_MAX=MAX(X_MAX, X )
	  ENDDO
	ENDDO
C
C Calculate what value of EXTI would give this correction
C
	EXTI=( (1.0+EXTI_CORR_MAX)**2 - 1.0)/X_MAX
C
	WRITE(10,'(A,2P,F5.1,A)') 'Estimated extinction parameter for '//
	1							'a max. correction of',EXTI_CORR_MAX,'%'
C
	RETURN
	END



